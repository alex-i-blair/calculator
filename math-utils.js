export function add(numIn1, numIn2) {
    return numIn1 + numIn2;
}
export function subtract(numIn1, numIn2) {
    return numIn1 - numIn2;
}
export function multiply(numIn1, numIn2) {
    return numIn1 * numIn2;
}
export function divide(numIn1, numIn2) {
    return numIn1 / numIn2;
}
export function exp(numIn1, numIn2) {
    return numIn1 ** numIn2;
}